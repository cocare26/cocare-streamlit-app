def chatbot_predict_ar(text):
    global conversation_state

    clean_text = normalize_text(text)

    # ========================
    # Follow-up handling
    # ========================
    if conversation_state["awaiting_followup"]:
        yes_words = {"نعم", "اه", "ايوه", "أكيد", "اكيد"}
        no_words = {"لا", "مو", "مش"}

        if clean_text in yes_words:
            conversation_state["awaiting_followup"] = False
            return {
                "text": text,
                "intent": "follow_up_response",
                "confidence": 1.0,
                "response": "تمام خلينا نكمل خطوة خطوة",
                "follow_up": None
            }

        elif clean_text in no_words:
            conversation_state["awaiting_followup"] = False
            return {
                "text": text,
                "intent": "follow_up_response",
                "confidence": 1.0,
                "response": "تمام، إذا احتجت أي شي بعدين أنا موجودة",
                "follow_up": None
            }

    # ========================
    # Rule-based override
    # ========================
    forced_intent = rule_based_override(text)

    if forced_intent is not None:
        predicted_intent = forced_intent
        confidence = 0.99

    else:
        pred = clf(clean_text, truncation=True)[0]
        predicted_intent = normalize_intent(pred["label"])
        confidence = float(pred["score"])

        # ========================
        # 🔥 BOOST (تحسين ذكي)
        # ========================
        if predicted_intent == "check_data_usage" and confidence < 0.60:
            confidence += 0.20

        elif predicted_intent == "network_status" and confidence < 0.60:
            confidence += 0.15

        elif predicted_intent == "slow_internet" and confidence < 0.60:
            confidence += 0.10

        # ========================
        # Threshold
        # ========================
        if confidence < 0.40:
            predicted_intent = "other"

    # ========================
    # Response
    # ========================
    response = response_map.get(predicted_intent, default_response_map.get(predicted_intent))
    follow_up = followup_map.get(predicted_intent, default_followup_map.get(predicted_intent))

    conversation_state["last_intent"] = predicted_intent
    conversation_state["awaiting_followup"] = True if follow_up and str(follow_up).strip() else False

    return {
        "text": text,
        "intent": predicted_intent,
        "confidence": confidence,
        "response": response,
        "follow_up": follow_up
    }
